@pytest.mark.restapi
@allure.feature(_FEATURE_PAGES)
@allure.title("Pages full lifecycle: folder → upload → search → rename → delete → stats unchanged")
def test_pages_full_lifecycle(cms_content_ops: CmsContentOperations, store_id: str, make_content_folder) -> None:
    content_type = "pages"

    with allure.step("GET initial pages count"):
        initial_pages_count = cms_content_ops.get_stats(store_id)["pagesCount"]

    with allure.step("POST /folder — create folder"):
        folder = make_content_folder(content_type=content_type)
        folder_name = folder["folderName"]

    with allure.step("POST upload file (multipart)"):
        cms_content_ops.upload_file(
            content_type=content_type,
            store_id=store_id,
            folder_url=folder_name,
            file_name=_PAGE_FILE_NAME,
            file_bytes=_PAGE_FILE_BYTES,
            file_content_type="text/markdown",
        )

    # Note: GET ?relativeUrl=... returns the *file content* (text/html) for .md files,
    # not JSON metadata — verifying via /search instead, which is what the Katalon
    # script also does immediately after upload.
    with allure.step("GET /search — locate uploaded file"):
        results = cms_content_ops.search(content_type=content_type, store_id=store_id, keyword=_PAGE_FILE_NAME)
        assert results, f"Uploaded file {_PAGE_FILE_NAME} not in search results"
        assert results[0]["name"] == _PAGE_FILE_NAME

    old_url = results[0]["url"]
    new_file_name = f"renamed-{_PAGE_FILE_NAME}"
    new_url = old_url.replace(_PAGE_FILE_NAME, new_file_name)

    with allure.step("GET /move — rename file"):
        cms_content_ops.move(content_type=content_type, store_id=store_id, old_url=old_url, new_url=new_url)

    with allure.step("GET /search — verify renamed file"):
        renamed = cms_content_ops.search(content_type=content_type, store_id=store_id, keyword=new_file_name)
        assert renamed and renamed[0]["name"] == new_file_name

    with allure.step("DELETE — remove the file"):
        cms_content_ops.delete(
            content_type=content_type,
            store_id=store_id,
            urls=f"/{folder_name}/{new_file_name}",
        )

    with allure.step("GET /search — file should be gone"):
        gone = cms_content_ops.search(content_type=content_type, store_id=store_id, keyword=new_file_name)
        assert not gone or gone[0]["name"] != new_file_name

    # folder cleanup happens in fixture teardown
    with allure.step("Wait for folder cleanup, verify final pages count == initial"):
        # explicit folder delete here so stats compare is meaningful inside the test
        cms_content_ops.delete(content_type=content_type, store_id=store_id, urls=folder_name)
        final_count = cms_content_ops.get_stats(store_id)["pagesCount"]
        assert (
            final_count == initial_pages_count
        ), f"pagesCount drifted: initial={initial_pages_count}, final={final_count}"


