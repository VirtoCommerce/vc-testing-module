@pytest.mark.restapi
@allure.feature(_FEATURE_VALIDATION)
@allure.title("Folder name validation: '{folder_name}' must be rejected")
@pytest.mark.parametrize(
    "folder_name",
    [
        pytest.param(" ", id="whitespace-only"),
        pytest.param("/etc/passwd", id="absolute-path"),
        pytest.param("..", id="parent-dir-traversal"),
        pytest.param("a" * 300, id="too-long"),
    ],
)
def test_folder_create_invalid_name(cms_content_ops: CmsContentOperations, store_id: str, folder_name: str) -> None:
    with allure.step(f"POST /folder name={folder_name!r} — expect rejection"):
        try:
            response = cms_content_ops.create_folder(content_type="pages", store_id=store_id, folder_name=folder_name)
        except requests.HTTPError as exc:
            assert exc.response.status_code >= 400
            return
        # If server returned 200 with an error body, it should at least contain a 'message'
        assert isinstance(response, dict), f"Unexpected non-error response: {response!r}"
        assert response.get("message") or response.get(
            "error"
        ), f"Server accepted invalid folder name {folder_name!r}: {response!r}"
        # cleanup if accepted
        try:
            cms_content_ops.delete(content_type="pages", store_id=store_id, urls=folder_name)
        except Exception as e:
            logger.warning("Cleanup failed: %s", e)


