@pytest.mark.restapi
@allure.feature(_FEATURE_MENU)
@allure.title(
    "Menu link list lifecycle: checkname-free → create → get-by-id → rename → checkname-taken → delete → gone"
)
@pytest.mark.xfail(
    strict=False,
    reason=(
        "Menu DELETE silently no-ops on this demo backend. Verified empirically: "
        "?ids=<guid> returns 204 but the menu remains in GET /menu; ?listIds=<guid> "
        "(per the .rs file) returns 500 NullReferenceException; ?ids[]= same as ?ids=. "
        "All other lifecycle steps (checkname, create, get-by-id, rename) succeed. "
        "Remove xfail when the backend is fixed."
    ),
)
def test_menu_lifecycle(menu_ops: MenuLinkOperations, store_id: str) -> None:
    initial = menu_ops.get_all(store_id) or []
    initial_count = len(initial)

    list_id = str(uuid.uuid4())
    menu_name = f"QAMenu_{uuid.uuid4().hex[:6]}"
    new_menu_name = f"{menu_name}Renamed"

    try:
        with allure.step(f"GET /checkname?name={menu_name} — expect free"):
            check = menu_ops.checkname(store_id=store_id, name=menu_name)
            assert check.get("result") is True, f"Expected name {menu_name} to be free, got {check}"

        with allure.step("POST — create menu list"):
            menu_ops.create_or_update(
                store_id=store_id,
                list_id=list_id,
                name=menu_name,
                menu_links=[
                    {"priority": 10, "title": "QweLinkTitle", "url": "~/pages/about_us"},
                    {"priority": 0, "title": "QweLinkTitle 2", "url": "~/pages"},
                ],
            )

        with allure.step("GET — created menu appears in list"):
            after_create = menu_ops.get_all(store_id) or []
            assert any(m.get("id") == list_id for m in after_create), "Created list missing"

        with allure.step("GET /{listId} — fetch by id"):
            by_id = menu_ops.get_by_id(store_id=store_id, list_id=list_id)
            assert by_id and by_id["name"] == menu_name

        with allure.step("POST — rename menu"):
            menu_ops.create_or_update(store_id=store_id, list_id=list_id, name=new_menu_name, menu_links=[])

        with allure.step(f"GET /checkname?name={new_menu_name} — expect taken"):
            check2 = menu_ops.checkname(store_id=store_id, name=new_menu_name)
            assert check2.get("result") is False, f"Expected name {new_menu_name} to be taken, got {check2}"

    finally:
        with allure.step("DELETE — remove menu list"):
            try:
                menu_ops.delete(store_id=store_id, list_ids=list_id)
            except Exception as e:
                logger.warning("Cleanup failed: %s", e)

    # Note: GET /{listId} on this backend returns a shell object after DELETE
    # (menuLinks=None, name preserved) rather than the null body the Katalon script
    # expected. Authoritative check is the list-level count returning to baseline.
    with allure.step("Verify deleted list is no longer in the list endpoint"):
        final = menu_ops.get_all(store_id) or []
        assert not any(
            m.get("id") == list_id for m in final
        ), f"Deleted list {list_id} still appears in menu list response"
        assert len(final) == initial_count, f"Menu count drifted: {initial_count} → {len(final)}"


