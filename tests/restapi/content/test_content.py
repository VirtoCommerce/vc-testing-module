@pytest.mark.restapi
@allure.feature(_FEATURE_BLOG)
@allure.title("Blog full lifecycle: folder → post → rename (forbidden + allowed) → delete → 404 → stats unchanged")
def test_blog_full_lifecycle(cms_content_ops: CmsContentOperations, store_id: str, make_content_folder) -> None:
    content_type = "blogs"

    with allure.step("GET initial blogs count"):
        initial_count = cms_content_ops.get_stats(store_id)["blogsCount"]

    with allure.step("POST /folder — create blog folder"):
        folder = make_content_folder(content_type=content_type)
        folder_name = folder["folderName"]

    with allure.step("POST upload blog post (multipart)"):
        cms_content_ops.upload_file(
            content_type=content_type,
            store_id=store_id,
            folder_url=folder_name,
            file_name=_PAGE_FILE_NAME,
            file_bytes=_PAGE_FILE_BYTES,
            file_content_type="text/markdown",
        )

    with allure.step("GET /search — locate post"):
        results = cms_content_ops.search(content_type=content_type, store_id=store_id, keyword=_PAGE_FILE_NAME)
        assert results, "Blog post missing after upload"
        old_url = results[0]["url"]
        relative_url = results[0]["relativeUrl"]

    new_file_name = f"renamed-{_PAGE_FILE_NAME}"
    forbidden_file_name = _PAGE_FILE_NAME.replace(".md", ".exe")
    new_url = old_url.replace(_PAGE_FILE_NAME, new_file_name)
    forbidden_url = old_url.replace(_PAGE_FILE_NAME, forbidden_file_name)

    with allure.step("GET /move to .exe — should fail (forbidden extension)"):
        with pytest.raises(requests.HTTPError) as excinfo:
            cms_content_ops.move(content_type=content_type, store_id=store_id, old_url=old_url, new_url=forbidden_url)
        assert (
            excinfo.value.response.status_code >= 400
        ), f"Expected error rename to .exe, got {excinfo.value.response.status_code}"

    with allure.step("GET /move to .md — should succeed"):
        cms_content_ops.move(content_type=content_type, store_id=store_id, old_url=old_url, new_url=new_url)

    with allure.step("GET /search — find renamed post"):
        renamed = cms_content_ops.search(content_type=content_type, store_id=store_id, keyword=new_file_name)
        assert renamed and renamed[0]["url"] == new_url

    with allure.step("DELETE renamed post"):
        cms_content_ops.delete(content_type=content_type, store_id=store_id, urls=new_url)

    with allure.step("GET via original relative URL — expect 404 / empty"):
        try:
            result = cms_content_ops.get(content_type=content_type, store_id=store_id, relative_url=relative_url)
            assert result is None or not result, f"Deleted post still returned: {result!r}"
        except requests.HTTPError as exc:
            assert exc.response.status_code in (404, 400)

    with allure.step("DELETE folder + verify blogsCount returned to initial"):
        cms_content_ops.delete(content_type=content_type, store_id=store_id, urls=folder_name)
        final_count = cms_content_ops.get_stats(store_id)["blogsCount"]
        assert final_count == initial_count, f"blogsCount drifted: initial={initial_count}, final={final_count}"


